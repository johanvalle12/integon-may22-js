function sumar(x, y){
    //suma = x + y;
    //console.log("La suma de " + x + " y " + y + " es de: " + suma);
    return parseInt(x) + parseInt(y);
}
function restar(x, y){
    // resta = x - y;
    // console.log("La resta de " + x + " y " + y + " es de: " + resta);
    return parseInt(x) - parseInt(y);
}
function multiplicar(x, y){
    // multiplicacion = x * y;
    // console.log("La multiplicacion de " + x + " y " + y + " es de: " + multiplicacion);
    return parseInt(x) * parseInt(y);
}
function dividir(x, y){
    // division = x + y;
    // console.log("La division de de " + x + " entre " + y + " es de: " + division);
    return parseInt(x) / parseInt(y);
}
